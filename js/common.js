$(document).ready(function() {

  // Navigation
  // Adjust the font size
	$(".small-font").click(function(){
		$("body").css("font-size","1em");
	});
	$(".normal-font").click(function(){
		$("body").css("font-size","2em");
	});
  $(".big-font").click(function(){
		$("body").css("font-size","3em");
	});

});
